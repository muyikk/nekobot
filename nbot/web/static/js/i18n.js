// 当前语言，从 localStorage 读取，默认为中文
let currentLanguage = localStorage.getItem('nbot-language') || 'zh';

/**
 * 获取翻译文本
 * @param {string} key - 翻译键
 * @param {object} params - 替换参数，如 {count: 5}
 * @returns {string} 翻译后的文本
 */
function t(key, params = {}) {
  const messages = i18nMessages[currentLanguage] || i18nMessages.zh;
  let text = messages[key] || i18nMessages.zh[key] || key;
  
  // 替换参数
  Object.keys(params).forEach(param => {
    text = text.replace(new RegExp(`{${param}}`, 'g'), params[param]);
  });
  
  return text;
}

/**
 * 切换语言
 * @param {string} lang - 语言代码 ('zh' 或 'en')
 */
function setLanguage(lang) {
  if (i18nMessages[lang]) {
    currentLanguage = lang;
    localStorage.setItem('nbot-language', lang);
    document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en';
    
    // 触发语言变更事件
    window.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
  }
}

/**
 * 获取当前语言
 * @returns {string} 当前语言代码
 */
function getLanguage() {
  return currentLanguage;
}

/**
 * 获取支持的语言列表
 * @returns {array} 语言列表
 */
function getSupportedLanguages() {
  return [
    { code: 'zh', name: '简体中文' },
    { code: 'en', name: 'English' }
  ];
}

// Vue 3 插件
const i18nPlugin = {
  install(app) {
    app.config.globalProperties.$t = t;
    app.config.globalProperties.$setLanguage = setLanguage;
    app.config.globalProperties.$getLanguage = getLanguage;
    app.config.globalProperties.$getSupportedLanguages = getSupportedLanguages;
    
    // 添加全局响应式数据
    app.config.globalProperties.$i18n = {
      locale: currentLanguage,
      setLocale: setLanguage,
      getLocale: getLanguage
    };
  }
};

